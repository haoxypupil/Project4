package com.siemens.entity;

public class Website {
	private int w_id;
	private String w_name;
	private String w_url;
	private int w_alexa;
	private String w_country;
	
	public int getW_id() {
		return w_id;
	}
	public void setW_id(int w_id) {
		this.w_id = w_id;
	}
	public String getW_name() {
		return w_name;
	}
	public void setW_name(String w_name) {
		this.w_name = w_name;
	}
	public String getW_url() {
		return w_url;
	}
	public void setW_url(String w_url) {
		this.w_url = w_url;
	}
	public int getW_alexa() {
		return w_alexa;
	}
	public void setW_alexa(int w_alexa) {
		this.w_alexa = w_alexa;
	}
	public String getW_country() {
		return w_country;
	}
	public void setW_country(String w_country) {
		this.w_country = w_country;
	}
	
	
	
}
